public class GradeCheck {
    public static void main(String[] args) {
        int arham = 76;
        if (arham >= 80)
            System.out.println("Grade A");
        else if (arham >= 60)
            System.out.println("Grade B");
        else if (arham >= 40)
            System.out.println("Grade C");
        else
            System.out.println("Fail");
    }
}
